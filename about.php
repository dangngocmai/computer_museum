    <h2 class = "what"> Who are we?</h2>
    <p> The University of Waterloo Computing Museum is university-based museum, whose purpose is to collect, preserve and interpret the history of computing at the University of Waterloo (UW) and related organizations of the geographic region. <br> <br>
        
       The time period it focuses on is primarily from 1957 (the year UW was established) to the present. The museum collects various computing related artifacts: hardware, software, documents, books, manuals, and other related cultural artifacts. <br><br>
       
          As appropriate, it collaborates and cooperates with other UW units that hold overlapping interests, such as the University Archives and Records Management or various Alumni offices.<br><br>
        
          The museum has physical and online exhibits, research, teaching, public events and publications. <br>  </p>
      
