    <h2 class = "what">History of Computer Museum</h2>
    <p>  Waterloo now is one of the main technical regions - "Canadian Sillicon Valley". Therefore, the development of technology is very important. In 2017, the Computer Science Department managers, Mr. Lawrence Folland  and Mr. Scott Campbell first came to the ideas making the university's very own exhibit. 
        <br> <br> They started to spend their free-time finding materials, including old discarded computers. The museum required a lot of time to manage the software, fix the computers and catalogue their artifacts. The old equipment went to central stores, and was later treated as e-waste. If it had value, they sold it right away.  <br><br>
                
 The museum has parts from the first mainframe on campus, dating all the way back to the mid-80s. There is a slide roll and a mechanical calculator from the 19th century, and even a WWII bombsight device. They are all displayed in Davis Centre.  <br></p>
      
      
